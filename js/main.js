let scene, camera, renderer, icosahedron, edges;
let isRotating = false;

async function init() {
  try {
    await setupScene();
    createIcosahedron();
    setupEventListeners();
    animate();
  } catch (error) {
    console.error("Initialization error:", error);
  }
}

async function setupScene() {
  scene = new THREE.Scene();
  scene.background = new THREE.Color(CONFIG.colors.bg);

  camera = new THREE.PerspectiveCamera(
    60,
    window.innerWidth / window.innerHeight,
    0.1,
    10
  );
  camera.position.z = 8;

  renderer = new THREE.WebGLRenderer({
    antialias: true,
    powerPreference: "high-performance",
  });
  renderer.setSize(window.innerWidth, window.innerHeight);
  renderer.setPixelRatio(window.devicePixelRatio);
  document.getElementById("sceneContainer").appendChild(renderer.domElement);

  // –û—Å–≤–µ—â–µ–Ω–∏–µ
  const ambientLight = new THREE.AmbientLight(0x404040);
  scene.add(ambientLight);

  const directionalLight = new THREE.DirectionalLight(0x7752fe, 1.5);
  directionalLight.position.set(2, 2, 2);
  scene.add(directionalLight);

  window.addEventListener("resize", onWindowResize);
}

function createIcosahedron() {
  // –°–æ–∑–¥–∞–µ–º –≥–µ–æ–º–µ—Ç—Ä–∏—é –∏–∫–æ—Å–∞—ç–¥—Ä–∞
  const geometry = new THREE.IcosahedronGeometry(2.5, 1);

  // –ú–∞—Ç–µ—Ä–∏–∞–ª –¥–ª—è –≥—Ä–∞–Ω–µ–π (–ø–æ–ª—É–ø—Ä–æ–∑—Ä–∞—á–Ω—ã–π)
  const material = new THREE.MeshPhongMaterial({
    color: CONFIG.colors.face,
    transparent: true,
    opacity: 0.1,
    specular: 0x7752fe,
    shininess: 50,
    side: THREE.DoubleSide,
  });

  icosahedron = new THREE.Mesh(geometry, material);
  scene.add(icosahedron);

  // –°–æ–∑–¥–∞–µ–º —Ä–µ–±—Ä–∞ —Å –ø–æ–º–æ—â—å—é EdgesGeometry
  const edgesGeometry = new THREE.EdgesGeometry(geometry);
  const edgesMaterial = new THREE.LineBasicMaterial({
    color: CONFIG.colors.edge,
    linewidth: 10,
    transparent: false, // –û—Ç–∫–ª—é—á–∞–µ–º –ø—Ä–æ–∑—Ä–∞—á–Ω–æ—Å—Ç—å –¥–ª—è —Å—Ç–∞–±–∏–ª—å–Ω–æ—Å—Ç–∏
  });

  edges = new THREE.LineSegments(edgesGeometry, edgesMaterial);
  scene.add(edges);
}

function animate() {
  requestAnimationFrame(animate);

  // –ü–ª–∞–≤–Ω–æ–µ –≤—Ä–∞—â–µ–Ω–∏–µ
  const rotationSpeed = isRotating ? 0.02 : 0.005;
  icosahedron.rotation.y += rotationSpeed;
  icosahedron.rotation.x += rotationSpeed;

  if (edges) edges.rotation.copy(icosahedron.rotation);

  renderer.render(scene, camera);
}

function setupEventListeners() {
  const askBtn = document.getElementById("askButton");
  const questionInput = document.getElementById("questionInput");
  const answerDisplay = document.getElementById("answerDisplay");

  askBtn.addEventListener("click", () => {
    if (!questionInput.value.trim()) {
      showAnswer("–ó–∞–¥–∞–π –≤–æ–ø—Ä–æ—Å...", answerDisplay);
      return;
    }

    startMagicAnimation(answerDisplay);
  });

  questionInput.addEventListener("keypress", (e) => {
    if (e.key === "Enter") askBtn.click();
  });
}

function startMagicAnimation(answerDisplay) {
  isRotating = true;

  // –°–æ—Ö—Ä–∞–Ω—è–µ–º –∏—Å—Ö–æ–¥–Ω—ã–µ –∑–Ω–∞—á–µ–Ω–∏—è
  const originalColor = edges.material.color.clone();
  const originalScale = {
    x: edges.scale.x,
    y: edges.scale.y,
    z: edges.scale.z,
  };

  // –ü–µ—Ä–≤–∞—è —Ñ–∞–∑–∞ –∞–Ω–∏–º–∞—Ü–∏–∏ - —É–≤–µ–ª–∏—á–µ–Ω–∏–µ –∏ —Å–º–µ–Ω–∞ —Ü–≤–µ—Ç–∞
  gsap.to(edges.scale, {
    x: 1.3,
    y: 1.3,
    z: 1.3,
    duration: 0.6,
    ease: "power2.out",
    onComplete: () => {
      // –í—Ç–æ—Ä–∞—è —Ñ–∞–∑–∞ - –≤–æ–∑–≤—Ä–∞—Ç –∫ –∏—Å—Ö–æ–¥–Ω–æ–º—É —Å–æ—Å—Ç–æ—è–Ω–∏—é
      gsap.to(edges.scale, {
        x: originalScale.x,
        y: originalScale.y,
        z: originalScale.z,
        duration: 0.6,
        ease: "power2.in",
      });

      // –ì–∞—Ä–∞–Ω—Ç–∏—Ä–æ–≤–∞–Ω–Ω—ã–π –≤–æ–∑–≤—Ä–∞—Ç —Ü–≤–µ—Ç–∞
      gsap.to(edges.material, {
        r: originalColor.r,
        g: originalColor.g,
        b: originalColor.b,
        duration: 0.6,
        ease: "power2.in",
        onComplete: () => {
          // –§–∏–∫—Å –¥–ª—è —Ä–µ–¥–∫–∏—Ö —Å–ª—É—á–∞–µ–≤
          edges.material.color.set(originalColor);
        },
      });
    },
  });

  // –ê–Ω–∏–º–∞—Ü–∏—è —Ü–≤–µ—Ç–∞ (–æ—Ç–¥–µ–ª—å–Ω–æ –¥–ª—è –Ω–∞–¥—ë–∂–Ω–æ—Å—Ç–∏)
  gsap.to(edges.material, {
    r: 1, // –ö—Ä–∞—Å–Ω—ã–π
    g: 0, // –ó–µ–ª—ë–Ω—ã–π
    b: 1, // –°–∏–Ω–∏–π (—Ñ–∏–æ–ª–µ—Ç–æ–≤—ã–π)
    duration: 0.3,
  });

  // –ü–æ–∫–∞–∑ –æ—Ç–≤–µ—Ç–∞
  setTimeout(() => {
    const randomAnswer =
      CONFIG.answers[Math.floor(Math.random() * CONFIG.answers.length)];
    showAnswer(randomAnswer, answerDisplay);
    isRotating = false;

    // –ê–±—Å–æ–ª—é—Ç–Ω–∞—è –≥–∞—Ä–∞–Ω—Ç–∏—è –≤–æ—Å—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω–∏—è
    edges.material.color.copy(originalColor);
    edges.scale.set(originalScale.x, originalScale.y, originalScale.z);
  }, 2000);
}
function showAnswer(text, element) {
  element.textContent = text;
  element.classList.add("visible");

  setTimeout(() => {
    element.classList.remove("visible");
  }, 5000);
}

function onWindowResize() {
  camera.aspect = window.innerWidth / window.innerHeight;
  camera.updateProjectionMatrix();
  renderer.setSize(window.innerWidth, window.innerHeight);
}

